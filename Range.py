"""
For + range
range => range(start, stop, step)
"""

numeros = range(10)

for numero in numeros:
    print(numero)

print("###########################")

numeros = range(5, 10)

for numero in numeros:
    print(numero)

print("###########################")

numeros = range(4, 11, 2)

for numero in numeros:
    print(numero)
