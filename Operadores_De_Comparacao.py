"""
Operadores de comparação (relacionais)
OP      Significado         Exemplo(true)
>       Maior               5 > 3
<       Menor               3 < 5
>=      Maior ou igual      5 >= 5
<=      Menor ou igual      2 <= 6
==      Igual               'a' == 'a'
!=      Diferente           'a' != 'b'
"""

maior = 5 > 3
menor = 3 < 5
maior_ou_igual = 5 >= 5
menor_ou_igual = 2 <= 6
igual = 'a' == 'a'
diferente = 'a' != 'b'

print(maior)
print(menor)
print(maior_ou_igual)
print(menor_ou_igual)
print(igual)
print(diferente)
