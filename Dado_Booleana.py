#Tipo de dado bool
#Existem apenas 2 respotas ao se questionar algo em um programa
#Sim(True) ou não (False)
#Ao se usar um operador logico como ==, por exemplo, pode se obter uma dessas respostas

print(10 == 10) # 10 é igual a 10? Sim -> True (Verdadeiro)
print(10 == 20) # 10 é igual a 20? Não => False (Falso)
print(type(10 == 10))
print(type(10 == 20))
