# Variaveis são formas de salvar algo na memoria do computador
# Imagine as variaveis como uma caixa que contem uma informação
# Não use caracteres especiais ou espaços quando nomear uma variavel
# Use apenas letras minusculas, numeros e underlines( _ )
# Para atribuir algo a uma variavel basta escreve-la e colocar a informação depois usando o sinal de igual (=)
# Exemplo:
# nome_completo = "Levi de Almeida Pontes"
# Por python ser uma linguagem do tipo dinamica, você não precisa especificar que tipo de variavel você está criando, o interpretador faz isso automaticamente
nome_completo = "Levi de Alemida Pontes"
print(type(nome_completo))
print(nome_completo)
