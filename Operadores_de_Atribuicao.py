"""
Operadores de atribuição
atribuir algo em uma variavel
= += -= /= //= *= **= %=
ao se colocar um operador aritmetico antecedendo o sinal de atribubição(=), pode-se atribuir com uma operação
Ex:
variavel = 1
variavel += 1 => Significa que variavel = variavel + 1, ou seja, 1 + 1 = 2
"""

contador = 0
print(contador)
contador += 2
print(contador)
contador *= 4
print(contador)
contador //= 2
print(contador)
contador **= 10
print(contador)
contador %= 10
print(contador)
contador -= 4
print(contador)
contador **= 20
print(contador)
